  /// Obtener todos los iconos únicos (sin duplicados visuales)
  ///
  /// Compara los iconos por su contenido para eliminar duplicados visuales,
  /// manteniendo solo el primer icono de cada grupo de duplicados.
  static List<Map<String, dynamic>> getUniqueIcons() {
    final seenIconContent = <String>{};
    final uniqueIcons = <Map<String, dynamic>>[];

    for (final iconData in _rawIcons) {
      final icon = iconData['icon'] as List<List<dynamic>>;

      // Convertir el contenido del icono a string para comparar visualmente
      // Esto compara el contenido real del path del SVG, no la referencia
      final iconContentString = _iconContentToString(icon);

      // Solo agregar si no lo hemos visto antes
      if (!seenIconContent.contains(iconContentString)) {
        seenIconContent.add(iconContentString);
        uniqueIcons.add(iconData);
      }
    }

    return uniqueIcons;
  }

  /// Convierte el contenido de un icono a string para comparación
  static String _iconContentToString(List<List<dynamic>> icon) {
    // Convertir cada path del icono a string
    final paths = icon.map((path) => path.toString()).toList();
    // Ordenar para que la comparación sea consistente
    paths.sort();
    return paths.join('|');
  }

  /// Obtener todos los iconos (incluyendo duplicados visuales)
  static List<Map<String, dynamic>> getAllIcons() {
    return List.from(_rawIcons);
  }
